package week07_10_22_2022;

public class Stars {
    public static void main(String[] args) {
        int numberOfColumn=7;
        int starNumberRow=4;


        for (int i = 1; i <= starNumberRow; i++) {
            for (int j = 0; j <= numberOfColumn ; j++) {
                System.out.print("*");
            }
            System.out.println();
        }








    }
}
