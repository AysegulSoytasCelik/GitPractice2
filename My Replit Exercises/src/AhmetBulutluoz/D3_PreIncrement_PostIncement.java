package AhmetBulutluoz;

public class D3_PreIncrement_PostIncement {
    public static void main(String[] args) {
        int num = 10;
        num ++; //post increment
        System.out.println(num); //11

        ++num; //preincrement
        System.out.println(num); //12

        System.out.println(num++); //post increment, still 12, next step will be 13
        System.out.println(num); //pre increment, this step increase +1 //13





    }
}
